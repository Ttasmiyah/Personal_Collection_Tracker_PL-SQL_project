SET VERIFY OFF;
		
BEGIN
	DBMS_OUTPUT.PUT_LINE(chr(10)||chr(10)||'Please Enter your Choice:');
	DBMS_OUTPUT.PUT_LINE('1. Search for any Stamp');
	DBMS_OUTPUT.PUT_LINE('2. Search for any Book');
	DBMS_OUTPUT.PUT_LINE(chr(10) || '-------Please Keep the unnecessary input fields blank--------');
END;
